from datetime import datetime, timezone

# 1. Текущее локальное время (наивное, зависит от настроек компьютера)
local_now = datetime.now()
print(f"Локальное время: {local_now}")

# 2. Текущее время в UTC (осведомленное, рекомендуемый стандарт)
utc_now = datetime.now(timezone.utc)
print(f"Время в UTC: {utc_now}")

# Вывод (зависит от момента запуска):
# Локальное время: 2025-09-25 18:30:00.123456
# Время в UTC: 2025-09-25 15:30:00.123456+00:00